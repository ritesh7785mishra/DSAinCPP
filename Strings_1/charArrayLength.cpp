#include <iostream>
using namespace std;
int main()
{
	char str[50];

	cin.getline(str,50,'\n');

	// Calculate the length of this char array 

	
	int i=0;



	while(str[i]!='\0')
	{
		i++;

	}	

	cout<<"Length of the char array is="<<i;

	// strlen is a inbuilt function to calculate length of
	// char array 
	cout<<"\nLength="<<strlen(str);



}